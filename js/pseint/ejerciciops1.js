// Crear un algoritmo que permita al usuario ingresar dos números, realizar la suma de ambos y mostrar el resultado.

const num1 = (parseInt(prompt("Ingrese el primer numero a sumar")));
const num2 = (parseInt(prompt("Ingrese el segundo numero a sumar")));
const suma = (num1 + num2);

document.writeln("El resultado de la suma es: "+suma);
